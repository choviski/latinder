                    ARTIGO 9:
                    Nenhum animal deve ser criado para servir de alimentação, deve ser nutrido, alojado,transportado e abatido,sem que para ele tenha ansiedade ou dor.

                    ARTIGO 10:
                    Nenhum animal deve ser usado para divertimento do homem. A exibição dos animais e os espetáculos que utilizem animais são incompatíveis com a dignidade do animal.

                    ARTIGO 11:
                    O ato que leva à morte de um animal sem necessidade é um biocídio, ou seja, um crime contra a vida.

                    ARTIGO 12:
                    Cada ato que leve à morte um grande número de animais selvagens é um genocídio, ou seja, um delito contra a espécie.
                    O aniquilamento e a destruição do meio ambiente natural levam ao genocídio.

                    ARTIGO 13:
                    O animal morto deve ser tratado com respeito.
                    As cenas de violência de que os animais são vítimas, devem ser proibidas no cinema e na televisão, a menos que tenham como fim mostrar um atentado aos direitos dos animais.

                    ARTIGO 14:
                    As associações de proteção e de salvaguarda dos animais devem ser representadas a nível de governo.
